#include "libft.h"

char *ft_strchr(const char *str, int c)
{
    if (!str)
        return (NULL);
    while (*str)
    {
        if ((char)c == *str)
            return (char *)str;
        str++; 
    }
    if ((char)c == '\0')
        return (char *)str;
    return NULL;
}

